const ISRAEL_TIMEZONE = 'Asia/Jerusalem';

function pad2(value: number): string {
  return String(value).padStart(2, '0');
}

function parseDateKey(dateKey: string) {
  const [year, month, day] = dateKey.split('-').map(Number);
  return { year, month, day };
}

function toDateKey(year: number, month: number, day: number): string {
  return `${year}-${pad2(month)}-${pad2(day)}`;
}

function dateKeyToUtcDate(dateKey: string, hour = 12): Date {
  const { year, month, day } = parseDateKey(dateKey);
  return new Date(Date.UTC(year, month - 1, day, hour, 0, 0));
}

function getIsraelDateParts(date: Date) {
  const parts = new Intl.DateTimeFormat('en-GB', {
    timeZone: ISRAEL_TIMEZONE,
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).formatToParts(date);

  const value = (type: Intl.DateTimeFormatPartTypes) =>
    Number(parts.find((part) => part.type === type)?.value ?? '0');

  return {
    year: value('year'),
    month: value('month'),
    day: value('day'),
  };
}

export function formatDate(date: string): string {
  return new Intl.DateTimeFormat('he-IL', {
    timeZone: ISRAEL_TIMEZONE,
    weekday: 'short',
    day: 'numeric',
    month: 'numeric',
  }).format(dateKeyToUtcDate(date));
}

export function formatDateFull(date: string): string {
  return new Intl.DateTimeFormat('he-IL', {
    timeZone: ISRAEL_TIMEZONE,
    weekday: 'long',
    day: 'numeric',
    month: 'long',
  }).format(dateKeyToUtcDate(date));
}

export function formatDateNumeric(dateKey: string): string {
  const { month, day } = parseDateKey(dateKey);
  return `${day}.${month}`;
}

export function getHebrewWeekdayLabel(dateKey: string): string {
  return LABELS.days[dateKeyToUtcDate(dateKey, 0).getUTCDay()];
}

export function getStatusColor(status: string): string {
  switch (status) {
    case 'pending': return 'bg-yellow-500/20 text-yellow-400';
    case 'scheduled': return 'bg-gray-500/20 text-gray-300';
    case 'active': return 'bg-green-500/20 text-green-400';
    case 'completed': return 'bg-blue-500/20 text-blue-400';
    case 'missed': return 'bg-red-500/20 text-red-400';
    case 'rejected': return 'bg-red-700/20 text-red-500 line-through';
    case 'cancelled': return 'bg-gray-600/20 text-gray-500 line-through';
    default: return 'bg-gray-500/20 text-gray-400';
  }
}

export function getStatusLabel(status: string): string {
  return LABELS[status as keyof typeof LABELS] as string || status;
}

export function getWeekDates(offset = 0, baseDate = new Date()): string[] {
  const today = getIsraelDateParts(baseDate);
  const todayUtc = new Date(Date.UTC(today.year, today.month - 1, today.day));
  const sunday = new Date(todayUtc);
  sunday.setUTCDate(todayUtc.getUTCDate() - todayUtc.getUTCDay() + offset * 7);

  return Array.from({ length: 7 }, (_, i) => {
    const d = new Date(sunday);
    d.setUTCDate(sunday.getUTCDate() + i);
    return toDateKey(d.getUTCFullYear(), d.getUTCMonth() + 1, d.getUTCDate());
  });
}

export function minutesUntil(date: string, time: string): number {
  const shiftStart = new Date(`${date}T${time}`);
  const now = new Date();
  return Math.floor((shiftStart.getTime() - now.getTime()) / 60000);
}

export function getPlatformBadge(platform: string | null): { label: string; className: string } {
  if (platform === 'telegram') return { label: 'TG', className: 'bg-blue-500/20 text-blue-400' };
  if (platform === 'onlyfans') return { label: 'OF', className: 'bg-orange-500/20 text-orange-400' };
  return { label: '', className: '' };
}

export function cn(...classes: (string | boolean | undefined | null)[]): string {
  return classes.filter(Boolean).join(' ');
}
